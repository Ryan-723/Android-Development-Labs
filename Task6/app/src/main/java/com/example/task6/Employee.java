package com.example.task6;

// Class representing an Employee
public class Employee {

    // Private fields for employee details
    private int id, salary;
    private String name, desig, dept, emailid;

    // Getter method for employee ID
    public int getId() {
        return id;
    }

    // Setter method for employee ID
    public void setId(int id) {
        this.id = id;
    }

    // Getter method for employee salary
    public int getSalary() {
        return salary;
    }

    // Setter method for employee salary
    public void setSalary(int salary) {
        this.salary = salary;
    }

    // Getter method for employee name
    public String getName() {
        return name;
    }

    // Setter method for employee name
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for employee designation
    public String getDesig() {
        return desig;
    }

    // Setter method for employee designation
    public void setDesig(String desig) {
        this.desig = desig;
    }

    // Getter method for employee department
    public String getDept() {
        return dept;
    }

    // Setter method for employee department
    public void setDept(String dept) {
        this.dept = dept;
    }

    // Getter method for employee email ID
    public String getEmailid() {
        return emailid;
    }

    // Setter method for employee email ID
    public void setEmailid(String emailid) {
        this.emailid = emailid;
    }
}
